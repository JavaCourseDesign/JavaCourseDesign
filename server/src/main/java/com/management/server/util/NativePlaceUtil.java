        String province = getNameString(provinceCode);
        String city = getNameString(cityCode);
        String district = getNameString(nativePlaceCode);
        String nativePlace = "";
        if (province != null) {
            nativePlace += province;
        }
        if (city != null) {
            nativePlace += city;
        }
        if (district != null) {
            nativePlace += district;
        }
        return nativePlace;
